//strlen() - returns length of string
<?php
    echo strlen("Hello! Welcome to GitHub!");
?>

//str_word_count() - count words in a string
<?php
    echo str_word_count("Hello! Welcome to GitHub!");
?>

//strrev() - reverse a string
<?php
    echo strrev("Hello! Welcome to GitHub!");
?>

strpos() - search for a text within a string
<?php
    echo strpos("Hello! Welcome to GitHub!", "to");
?>

//str_replace() - replace text within a string
<?php
    echo str_replace("Hello! Welcome to GitHub!", "Github", "programming");
?>
